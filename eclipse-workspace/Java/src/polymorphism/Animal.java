package polymorphism;

public class Animal {
/*
 polymorphism is one the oops principle 
 one object showing different behavior at different stage is called polymorphism
 its classified into 2 type Runtime and compile time
 */
	
	public void AnimalSound()
	{
		System.out.println("The animal makes a sound");
	}
	
	public void eatAnimal()
	{
		System.out.println("The animal eating food");
	}
	
	public void sleep()
	{
		System.out.println("Animal are sleeping");
	}
}
