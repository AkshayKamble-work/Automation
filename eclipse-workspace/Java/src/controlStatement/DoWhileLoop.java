package controlStatement;

public class DoWhileLoop
{

	
	public static void main(String[] args) 
	{
		
		int i=0;
		do {
			System.out.println(i);
			i++;
			
		} 
		while (i<10);
		System.out.println("************");
		
		
		int countdown = 3;

		while (countdown > 0) {
		  System.out.println(countdown);
		  countdown--;
		}

		System.out.println("Happy New Year!!");
	}

}
